#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char			*ret;	

	if (!s1 || !s2)
		return (0);
	ret = malloc((ft_strlen(s1) + ft_strlen(s2) + 1) * sizeof(char));
	if (!ret)
		return (0);
	ft_strlcpy(ret, s1, ft_strlen(s1) + 1);
	ft_strlcat(ret, s2, ft_strlen(s1) + ft_strlen(s2) + 1);
	return (ret);
}
